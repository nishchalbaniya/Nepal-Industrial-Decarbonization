"""
I/O for kiln simulation results.

Supports CSV (per-time, per-zone trajectories), JSON (engineered outputs),
pickle (full state for re-loading), and emits MATLAB / Octave scripts that
re-implement the same model in those languages for cross-validation.
"""
from __future__ import annotations

import json
import os
import pickle
from pathlib import Path
from typing import Any, Dict, Optional

import numpy as np

from .kiln_ode import KilnParameters, KilnState


def save_results_csv(path: str, t: np.ndarray, y: np.ndarray,
                     x: np.ndarray, p: KilnParameters) -> str:
    """Save full trajectory as a wide-format CSV.

    Columns: time_s, x_m, T_solid_K, T_gas_K, T_wall_K, conversion, CO2, O2
    """
    n = p.n_zones
    rows = []
    header = ["time_s", "x_m", "T_solid_K", "T_gas_K", "T_wall_K",
              "conversion", "CO2", "O2"]
    for ti in range(len(t)):
        for zi in range(n):
            rows.append([
                float(t[ti]),
                float(x[zi]),
                float(y[6 * zi + 0, ti]),
                float(y[6 * zi + 1, ti]),
                float(y[6 * zi + 2, ti]),
                float(y[6 * zi + 3, ti]),
                float(y[6 * zi + 4, ti]),
                float(y[6 * zi + 5, ti]),
            ])
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8", newline="") as f:
        f.write(",".join(header) + "\n")
        for r in rows:
            f.write(",".join(f"{v:.6g}" for v in r) + "\n")
    return path


def load_results_csv(path: str) -> Dict[str, np.ndarray]:
    """Load a CSV produced by save_results_csv. Returns column dict."""
    import csv
    cols: Dict[str, list] = {}
    with open(path, "r", encoding="utf-8") as f:
        reader = csv.DictReader(f)
        for row in reader:
            for k, v in row.items():
                cols.setdefault(k, []).append(float(v))
    return {k: np.array(v) for k, v in cols.items()}


def save_results_json(path: str, state: KilnState, p: KilnParameters,
                      outputs: Dict[str, float]) -> str:
    """Save engineered outputs as JSON (for dashboards, MRV)."""
    payload = {
        "tool": "nepal_kiln_sim",
        "version": "0.2.0",
        "parameters": json.loads(p.model_dump_json()),
        "state_summary": state.summary(),
        "outputs": outputs,
    }
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    with open(path, "w", encoding="utf-8") as f:
        json.dump(payload, f, indent=2, default=_json_default)
    return path


def _json_default(o: Any) -> Any:
    if isinstance(o, np.ndarray):
        return o.tolist()
    if isinstance(o, (np.floating,)):
        return float(o)
    if isinstance(o, (np.integer,)):
        return int(o)
    if isinstance(o, (np.bool_,)):
        return bool(o)
    return str(o)


def save_state_pickle(path: str, state: KilnState, t: np.ndarray,
                      y: np.ndarray, p: KilnParameters) -> str:
    Path(path).parent.mkdir(parents=True, exist_ok=True)
    with open(path, "wb") as f:
        pickle.dump({"state": state, "t": t, "y": y, "p": p}, f)
    return path


def load_state_pickle(path: str) -> Dict[str, Any]:
    with open(path, "rb") as f:
        return pickle.load(f)


